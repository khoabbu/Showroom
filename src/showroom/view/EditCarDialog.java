    public void loadCarData(Car car) {
    if (car != null) {
        this.editingCarId = car.getId(); // Lưu lại ID
        txtCarName.setText(car.getCarName());
        txtManufacturer.setText(car.getManufacturer());
        txtYear.setText(String.valueOf(car.getYearOfManufacture()));
        txtColor.setText(car.getColor());
        txtModelType.setText(car.getModelType());
        txtPrice.setText(String.valueOf(car.getSellingPrice()));
        txtQuantity.setText(String.valueOf(car.getQuantityInStock()));
        txtDescription.setText(car.getDescription());
selectedImagePath = car.getImagePath(); // Lưu lại đường dẫn ảnh cũ
        if (selectedImagePath != null && !selectedImagePath.isEmpty()) {
            // Chỉ lấy tên file từ đường dẫn
            lblDuongDanAnh.setText(new File(selectedImagePath).getName());
        } else {
            lblDuongDanAnh.setText("Chưa có ảnh");
        }

    }
}
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       try {
        // 1. Lấy dữ liệu từ các ô JTextField
        String carName = txtCarName.getText().trim();
        String manufacturer = txtManufacturer.getText().trim();
        // ... (toàn bộ phần lấy dữ liệu và kiểm tra lỗi giữ nguyên như trong AddCarDialog)
        String yearStr = txtYear.getText().trim();
        String color = txtColor.getText().trim();
        String modelType = txtModelType.getText().trim();
        String priceStr = txtPrice.getText().trim();
        String quantityStr = txtQuantity.getText().trim();
        String description = txtDescription.getText().trim();

        // 2. Kiểm tra dữ liệu đầu vào (Validation - bạn có thể copy từ AddCarDialog)
        if (carName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên xe không được để trống!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        double price = Double.parseDouble(priceStr);
        int quantity = Integer.parseInt(quantityStr);
        int year = Integer.parseInt(yearStr);

        // 3. Tạo đối tượng Car mới với dữ liệu đã cập nhật
        Car updatedCar = new Car();
        updatedCar.setId(this.editingCarId); // **QUAN TRỌNG: Gán ID của xe đang sửa**
        updatedCar.setCarName(carName);
        updatedCar.setManufacturer(manufacturer);
        updatedCar.setYearOfManufacture(year);
        updatedCar.setColor(color);
        updatedCar.setModelType(modelType);
        updatedCar.setSellingPrice(price);
        updatedCar.setQuantityInStock(quantity);
        updatedCar.setDescription(description);

        updatedCar.setImagePath(selectedImagePath);

        // 4. Gọi DAO để cập nhật vào CSDL
        CarDAO carDAO = new CarDAO();
        carDAO.updateCar(updatedCar); // **QUAN TRỌNG: Gọi updateCar thay vì addCar**

        // 5. Thông báo thành công và đóng cửa sổ
        JOptionPane.showMessageDialog(this, "Cập nhật xe thành công!");
        this.dispose(); // Đóng cửa sổ dialog này

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng số cho Năm SX, Giá bán và Số lượng.", "Lỗi định dạng", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi không mong muốn: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnSaveActionPerformed

